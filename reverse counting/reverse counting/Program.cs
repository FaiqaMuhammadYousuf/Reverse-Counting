using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace reverse_counting
{
    internal class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Reverse Counting");
            for (int i = 20; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
